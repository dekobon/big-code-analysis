//! Baseline file for `bca check`: external record of currently-known
//! threshold offenders that the check is allowed to ignore until the
//! team gets around to fixing them. Companion to (not substitute for)
//! the in-source suppression markers from issue #98 — suppressions
//! express "exempt forever", baselines express "tech debt we're paying
//! down".
//!
//! The on-disk shape is TOML, sorted by `(path, qualified, start_line,
//! metric)` so diffs are reviewable. Each entry records `(path,
//! qualified, start_line, metric, value)` plus an optional `body_hash`;
//! matching keys on `(path, qualified, metric)` with `start_line` as a
//! tolerance-band disambiguator (issue #377), so a function survives
//! line drift from edits above it. The filter is "current value <=
//! baseline value", so improvements pass silently and regressions
//! still fail.
//!
//! Path keys are canonicalised relative to an *anchor*: the directory
//! containing the baseline file (lexically resolved, no symlink
//! following). Write and read use the same anchor, so a baseline
//! generated by `bca check --paths .` matches a subsequent
//! `bca check --paths "$PWD"` byte-for-byte at the key level. See
//! [`normalize_path`] for the encoding pipeline.

use std::collections::HashMap;
use std::path::{Component, Path, PathBuf};

use serde::{Deserialize, Serialize};

use crate::thresholds::Violation;

/// Schema version. Bump on breaking format changes.
///
/// History:
/// - v1 → v2: path-key percent-encoding made total (the UTF-8 fast
///   path now escapes `%`, so `src/é.rs` encodes as `src/%C3%A9.rs`).
/// - v2 → v3: path keys canonicalised relative to the baseline-file
///   anchor (issue #376). `--paths .`, `--paths src/`, and
///   `--paths "$PWD"` produce byte-identical baselines because every
///   path is resolved to an absolute, lexically-normalised form and
///   then stripped against the anchor before percent-encoding.
/// - v3 → v4: entries key on the *qualified symbol* (`function` field
///   renamed to `qualified`, e.g. `MyStruct::do_thing`) plus a
///   `start_line` *tolerance* disambiguator (issue #377), rather than
///   the exact `start_line`. Editing code above a function no longer
///   re-keys it as a new offender. An optional `body_hash` field
///   carries a normalised body digest used as a rename-tolerant
///   fallback when `--baseline-fuzzy-match` is active.
///
/// Legacy baselines are still accepted at read time:
/// - v2: each entry's path string is re-run through the canonical
///   pipeline (a no-op for already-canonical ASCII paths).
/// - v2/v3: the entry's `function` field (read via a serde alias into
///   `qualified`) holds a *bare* name, so matching for a legacy file
///   degrades to bare-name + line-tolerance — equivalent to the old
///   behaviour but now drift-tolerant. Full qualified matching kicks
///   in once the file is refreshed with `--write-baseline`.
///
/// - v4 → v5: an optional `[provenance]` table records the tier
///   (`hard`|`soft`) and, for a `--headroom`-scaled soft baseline, the
///   `headroom` ratio it was written against (issue #486). This lets
///   `bca check` warn when the current run's effective limits are
///   *stricter* than the baseline was written for — the silent-desync
///   the "baseline-refresh discipline" guards against (#449). The
///   table is omitted from baselines whose provenance is unknown.
///
/// Either legacy version emits a one-time deprecation warning so the
/// user knows to refresh.
pub(crate) const BASELINE_VERSION: u32 = 5;

/// Lowest legacy version still accepted at read time. Below this we
/// reject with a "regenerate" hint instead of silently mis-matching.
const LEGACY_MIN_VERSION: u32 = 2;

/// First format version that keys entries by qualified symbol
/// (`Struct::method`) instead of a bare function name (issue #376's
/// follow-up, `d469108b`). Baselines below this stored only the bare
/// name, so a current run's qualified violation must be matched by its
/// innermost segment. Pinned to the format boundary — NOT
/// `BASELINE_VERSION` — so later version bumps (e.g. the v5 provenance
/// table, #486) do not silently widen bare-name matching to cover
/// versions that already store qualified names.
const QUALIFIED_SYMBOL_MIN_VERSION: u32 = 4;

/// Default tolerance (in lines) for the `start_line` disambiguator when
/// two baseline entries share the same `(path, qualified, metric)` key
/// — overloads, duplicate `impl` blocks, or collided anonymous spaces.
/// A function whose recorded line is within this many lines of the
/// observed one is treated as the same function. Overridable via
/// `--baseline-line-tolerance` / the `baseline_line_tolerance` manifest
/// key. 50 lines comfortably absorbs a block of imports or a sibling
/// function added above without colliding distinct same-named methods,
/// which are normally hundreds of lines apart.
pub(crate) const DEFAULT_LINE_TOLERANCE: usize = 50;

const HEADER: &str = "\
# bca baseline file. Generated by `bca check --write-baseline`.
# Listed offenders are filtered from threshold checks; a function that
# gets worse than its recorded value still fails. Refresh with
# `--write-baseline` when entries become stale.
#
# Path keys are relative to this file's directory (the anchor), so the
# baseline survives any `--paths` form (`.`, `src/`, `$PWD`, …).
";

/// Which gate tier a baseline was written against (issue #486). Stored
/// as the lowercase wire strings `"hard"` / `"soft"` in the
/// `[provenance]` table so `bca diff-baseline` and external tooling can
/// read it without knowing the enum.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub(crate) enum BaselineTier {
    Hard,
    Soft,
}

/// Write-time provenance for a baseline: the effective tier and, for a
/// `--headroom`-scaled soft baseline, the ratio (issue #486).
///
/// `headroom` is `Some` only for the soft tier scaled by an explicit or
/// default `--headroom` scalar. It is `None` for the hard tier (no
/// scaling) and for a soft tier driven by a `[thresholds.soft]` table
/// (per-metric limits that do not reduce to a single ratio). The pair
/// reduces to the [`Provenance::strictness`] scalar used by the
/// directional mismatch check.
#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub(crate) struct Provenance {
    pub(crate) tier: BaselineTier,
    /// The `--headroom` ratio for a scaled soft baseline. Omitted from
    /// the TOML when absent so hard and soft-table baselines stay
    /// compact.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub(crate) headroom: Option<f64>,
}

impl Provenance {
    /// Provenance for a hard-tier write (the default `bca check` gate).
    pub(crate) fn hard() -> Self {
        Self {
            tier: BaselineTier::Hard,
            headroom: None,
        }
    }

    /// Provenance for a soft-tier write scaled by `headroom`.
    pub(crate) fn soft_headroom(headroom: f64) -> Self {
        Self {
            tier: BaselineTier::Soft,
            headroom: Some(headroom),
        }
    }

    /// Provenance for a soft-tier write driven by a `[thresholds.soft]`
    /// table (per-metric limits, no single ratio).
    pub(crate) fn soft_table() -> Self {
        Self {
            tier: BaselineTier::Soft,
            headroom: None,
        }
    }

    /// Reduce the tier/headroom pair to a single *effective strictness*
    /// scalar in `(0, 1]` where smaller means stricter (the gate trips on
    /// lower metric values): hard → `1.0`; soft scaled by ratio `h` →
    /// `h`.
    ///
    /// Returns `None` for a soft `[thresholds.soft]`-table baseline,
    /// whose per-metric limits do not collapse to one ratio. The
    /// directional mismatch check skips comparison when either side is
    /// `None` rather than guess — conservative, so it never false-fires.
    pub(crate) fn strictness(&self) -> Option<f64> {
        match self.tier {
            BaselineTier::Hard => Some(1.0),
            BaselineTier::Soft => self.headroom,
        }
    }
}

/// Outcome of comparing the current run's provenance against the
/// baseline's (issue #486). Only [`StricterThanBaseline`] is actionable;
/// the others are silent.
///
/// [`StricterThanBaseline`]: ProvenanceCheck::StricterThanBaseline
#[derive(Debug, Clone, Copy, PartialEq)]
pub(crate) enum ProvenanceCheck {
    /// The current run is no stricter than the baseline (equal, or the
    /// safe hard-reading-soft direction), or one side's strictness is
    /// unknown (absent provenance / soft-table). No warning.
    Ok,
    /// The current run's effective limits are *stricter* than the
    /// baseline was written against, so the baseline may under-cover and
    /// the gate can fire on untouched files. Carries both strictness
    /// scalars for the warning message.
    StricterThanBaseline { current: f64, baseline: f64 },
}

/// Compare the current run's `current` provenance against the
/// `baseline`'s recorded provenance (issue #486).
///
/// Warn *only* when the current run is strictly stricter than the
/// baseline (`current` scalar `<` `baseline` scalar): the baseline was
/// written looser, so it may not list every offender the current,
/// tighter gate produces — the silent-fire-on-untouched-files bug the
/// baseline-refresh discipline guards against. The reverse direction is
/// safe: a hard run (scalar `1.0`) reading a soft baseline (scalar
/// `0.95`) sees a *superset* of its offenders, so it stays silent. So
/// does the equal case and any case where a side's strictness is
/// unknown (absent provenance — legitimate for pre-v5 files — or a
/// soft-table baseline).
pub(crate) fn check_provenance(
    current: Provenance,
    baseline: Option<Provenance>,
) -> ProvenanceCheck {
    let (Some(baseline), Some(current)) =
        (baseline.and_then(|p| p.strictness()), current.strictness())
    else {
        return ProvenanceCheck::Ok;
    };
    if current < baseline {
        ProvenanceCheck::StricterThanBaseline { current, baseline }
    } else {
        ProvenanceCheck::Ok
    }
}

/// Primary lookup key: a function's identity independent of where it
/// sits in the file (issue #377). Owned strings because deserialized
/// entries don't borrow from anything; the `Violation` path is
/// normalized to a forward-slash `String` on construction so baselines
/// committed from Linux match the same tree analyzed on Windows.
#[derive(Debug, Clone, Eq, PartialEq, Hash)]
struct SymbolKey {
    path: String,
    qualified: String,
    metric: String,
}

/// One in-memory baseline record under a [`SymbolKey`]. A single key can
/// map to several of these when a qualified symbol is genuinely
/// ambiguous (overloads, two `impl` blocks defining the same method,
/// collided anonymous spaces); `start_line` then disambiguates within a
/// tolerance band.
#[derive(Debug, Clone)]
struct Record {
    start_line: usize,
    value: f64,
    /// Normalised body digest, present only for v4 entries written with
    /// fuzzy matching enabled. Feeds the rename-tolerant fallback.
    body_hash: Option<u64>,
}

/// One baseline entry flattened for cross-file diffing
/// (`bca diff-baseline`): the `(path, qualified, metric)` identity plus
/// the recorded `value` and the human-review `start_line`. Produced by
/// [`Baseline::diff_entries`] *after* version validation, legacy path
/// re-canonicalisation, and the non-finite/negative filter have run, so
/// a diff observes exactly the records the matcher would.
#[derive(Debug, Clone)]
pub(crate) struct DiffEntry {
    pub(crate) path: String,
    pub(crate) qualified: String,
    pub(crate) metric: String,
    pub(crate) start_line: usize,
    pub(crate) value: f64,
}

/// One serialized entry. `path` is held as `String` (forward-slash
/// normalized) so the TOML is platform-neutral. Fields are
/// module-private; serde reaches them through the derived impls.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub(crate) struct BaselineEntry {
    path: String,
    /// Qualified symbol (`MyStruct::do_thing`). Read via the `function`
    /// alias from legacy v2/v3 files, where it held only the bare name.
    #[serde(alias = "function")]
    qualified: String,
    /// 1-based start line. No longer part of the identity key; retained
    /// for human review, deterministic ordering, and as the tolerance
    /// disambiguator for ambiguous qualified symbols.
    start_line: usize,
    metric: String,
    /// Metric value at baseline time. `current > value` still fails
    /// (ratchet-down). Non-finite values are skipped at construction.
    value: f64,
    /// Lowercase-hex `u64` digest of the normalised function body,
    /// written only when `--baseline-fuzzy-match` is active. Omitted
    /// from the TOML when absent so non-fuzzy baselines stay compact.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    body_hash: Option<String>,
}

/// Top-level baseline file. `version` is required; missing field is a
/// hard error so a truncated file isn't silently treated as empty.
///
/// `entries` is the only field reached from outside the module
/// (`run_check` reports the entry count after a `--write-baseline`).
///
/// `Default` is intentionally NOT derived: a default-constructed
/// `BaselineFile` would have `version: None` and render to TOML
/// without a version field, which `from_str` would then reject.
/// Construct explicitly with `Some(BASELINE_VERSION)` instead.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub(crate) struct BaselineFile {
    /// Schema version. Higher values mean the file was written by a
    /// newer bca and must be regenerated (or this bca upgraded).
    version: Option<u32>,
    /// Write-time tier/headroom provenance (issue #486, v5+). Absent for
    /// v2–v4 baselines, which predate the field; treated as "unknown" by
    /// the directional mismatch check so old files draw no spurious
    /// warning.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    provenance: Option<Provenance>,
    #[serde(default, rename = "entry")]
    pub(crate) entries: Vec<BaselineEntry>,
}

/// In-memory lookup. Construction validates the schema version and
/// drops entries with non-finite values (defense in depth — `toml`
/// rejects nan/inf at the parser level, but a malicious or hand-edited
/// file could still try).
///
/// `anchor` is the lexically-absolute directory the baseline lives in
/// (see [`anchor_for`]). Every classify call canonicalises the
/// incoming violation path against the same anchor that the loader
/// used, so the key shape matches regardless of which `--paths` form
/// the analyser was invoked with.
#[derive(Debug)]
pub(crate) struct Baseline {
    /// `(path, qualified, metric)` → the records under that symbol. Most
    /// keys map to exactly one record; a `Vec` only grows when the
    /// qualified symbol is ambiguous (see [`Record`]).
    by_symbol: HashMap<SymbolKey, Vec<Record>>,
    /// `(path, metric, body_hash)` → recorded value, populated only from
    /// entries carrying a `body_hash`. The rename-tolerant fallback used
    /// when `fuzzy` is on and the qualified symbol did not match.
    /// Last-write-wins on a key clash: two entries with an identical
    /// normalised body (same `(path, metric)`) share a value anyway, and
    /// a genuine 64-bit FNV collision between distinct bodies is
    /// negligible and at worst classifies one violation `Covered`
    /// instead of `New` — never a panic or miscount.
    by_hash: HashMap<(String, String, u64), f64>,
    anchor: PathBuf,
    /// Line tolerance for the disambiguator (see [`DEFAULT_LINE_TOLERANCE`]).
    tolerance: usize,
    /// Whether the fuzzy body-hash fallback is active (`--baseline-fuzzy-match`).
    fuzzy: bool,
    /// True for v2/v3 files, whose `qualified` field is really a bare
    /// name. Classify then compares against the violation's bare name
    /// (the last `::` segment) so a legacy baseline keeps matching until
    /// it is refreshed to the full qualified form.
    legacy_symbol_match: bool,
    /// Write-time provenance (issue #486), `None` for v2–v4 files that
    /// predate the field. Read by `bca check` to warn when the current
    /// run is stricter than the baseline was written against.
    provenance: Option<Provenance>,
}

impl Baseline {
    /// Parse a TOML baseline document keyed against `anchor` (typically
    /// derived via [`anchor_for`] from the baseline file's own path),
    /// applying `tolerance` for the start-line disambiguator and enabling
    /// the body-hash fallback when `fuzzy` is set. Errors are user-facing
    /// strings suitable for `die_io`.
    ///
    /// Legacy (v2/v3) baselines are accepted for backwards compatibility:
    /// - v2 entry paths are re-run through the canonical pipeline so the
    ///   path-stickiness fix (issue #376) applies retroactively. v2
    ///   entries whose stored path was already percent-encoded (non-ASCII
    ///   or special characters) may double-encode and surface as `New`
    ///   until the user refreshes.
    /// - v2/v3 entries carry a bare `function` name (read via the serde
    ///   alias into `qualified`); matching degrades to bare-name +
    ///   tolerance via `legacy_symbol_match`.
    ///
    /// A one-time deprecation warning is emitted on stderr recommending
    /// `--write-baseline` to upgrade the file in place.
    pub(crate) fn from_str(
        text: &str,
        anchor: &Path,
        tolerance: usize,
        fuzzy: bool,
    ) -> Result<Self, String> {
        let file: BaselineFile =
            toml::from_str(text).map_err(|e| format!("malformed baseline TOML: {e}"))?;
        let version = file
            .version
            .ok_or_else(|| "baseline missing version field".to_string())?;
        if !(LEGACY_MIN_VERSION..=BASELINE_VERSION).contains(&version) {
            return Err(format!(
                "baseline version {version} is not supported by this bca \
                 (expected {LEGACY_MIN_VERSION}..={BASELINE_VERSION}); \
                 regenerate with `bca check --write-baseline` or upgrade bca"
            ));
        }
        // v2/v3 store a bare function name and (v2 only) pre-canonical
        // paths. Below v3 the path form predates issue #376 and must be
        // re-canonicalised; v3 paths are already canonical and must NOT
        // be re-encoded (that would double-escape any `%XX`).
        let legacy_symbol_match = version < QUALIFIED_SYMBOL_MIN_VERSION;
        let recanonicalize_path = version < 3;
        // Provenance (issue #486) is v5+. v2–v4 carry none; the check
        // treats absent provenance as "unknown" and stays silent — old
        // files legitimately predate the field, and the v<BASELINE_VERSION
        // refresh hint below already nudges them to upgrade.
        let provenance = file.provenance;
        if legacy_symbol_match {
            eprintln!(
                "bca: baseline is v{version}; refresh with `--write-baseline` \
                 to migrate to v{BASELINE_VERSION} (qualified-symbol matching, \
                 survives line drift)"
            );
        }
        // Defensive: re-normalise the supplied anchor so a caller that
        // bypasses `anchor_for` (or hand-constructs an anchor with
        // `.`/`..` components) still gets correct strip_prefix matching.
        // Production code always routes through `anchor_for`, which is
        // already idempotent under `lexical_normalize`.
        let anchor = lexical_normalize(anchor);
        let mut by_symbol: HashMap<SymbolKey, Vec<Record>> = HashMap::new();
        let mut by_hash = HashMap::new();
        for e in file.entries {
            // Skip non-finite and negative values. Non-finite is the
            // pre-existing defense against hand-edited files (TOML's
            // parser also rejects nan/inf). Negative is the same idea
            // for the rendered-tag path: `format_regressed_tag` divides
            // by `recorded` to compute `+N%`, and a negative `recorded`
            // produces a double-signed `[regr +-N%]` tag. Metric
            // extractors never emit negative values, so the only way
            // an entry can be negative is hand-editing — silently
            // drop and treat the entry as if missing (the violation
            // will surface as `New`).
            //
            // `is_sign_negative()` catches `-0.0` too (which `< 0.0`
            // misses under IEEE 754: `-0.0 < 0.0` is false). `-0.0`
            // itself is benign for the renderer (`recorded == 0.0`
            // hits the `[regr from 0]` early-return in
            // `format_regressed_tag`), but dropping it here keeps the
            // write-side and read-side filters bit-symmetric: a
            // round-trip through `from_violations` -> `from_str` of
            // any caller-constructed value preserves the same set of
            // surviving entries.
            if !e.value.is_finite() || e.value.is_sign_negative() {
                continue;
            }
            let path = if recanonicalize_path {
                normalize_path(&anchor, Path::new(&e.path))
            } else {
                e.path
            };
            // A malformed hex digest (hand-edited file) decodes to None
            // rather than aborting the load — the entry simply loses its
            // fuzzy-fallback eligibility, exactly as if it were written
            // without `--baseline-fuzzy-match`.
            let body_hash = e.body_hash.as_deref().and_then(decode_body_hash);
            if let Some(h) = body_hash {
                by_hash.insert((path.clone(), e.metric.clone(), h), e.value);
            }
            by_symbol
                .entry(SymbolKey {
                    path,
                    qualified: e.qualified,
                    metric: e.metric,
                })
                .or_default()
                .push(Record {
                    start_line: e.start_line,
                    value: e.value,
                    body_hash,
                });
        }
        Ok(Self {
            by_symbol,
            by_hash,
            anchor,
            tolerance,
            fuzzy,
            legacy_symbol_match,
            provenance,
        })
    }

    /// The write-time provenance recorded in the baseline (issue #486),
    /// or `None` for a v2–v4 file that predates the field. Drives the
    /// directional mismatch warning in `bca check`.
    pub(crate) fn provenance(&self) -> Option<Provenance> {
        self.provenance
    }

    /// Flatten the loaded records into [`DiffEntry`] values for
    /// `bca diff-baseline`. Iteration order is unspecified (it walks a
    /// `HashMap`); the diff layer re-sorts deterministically.
    ///
    /// Legacy (v2/v3) baselines surface their bare `function` name in
    /// `qualified`, so a legacy-vs-v4 diff pairs only on identical
    /// bare/qualified strings — the inherent limit of comparing across
    /// the issue-#377 key migration without re-analysing source. Paths
    /// are already re-canonicalised by [`Baseline::from_str`], so a v2
    /// baseline diffs against a v4 one on equal path keys.
    pub(crate) fn diff_entries(&self) -> Vec<DiffEntry> {
        self.by_symbol
            .iter()
            .flat_map(|(key, records)| {
                records.iter().map(|r| DiffEntry {
                    path: key.path.clone(),
                    qualified: key.qualified.clone(),
                    metric: key.metric.clone(),
                    start_line: r.start_line,
                    value: r.value,
                })
            })
            .collect()
    }

    /// Classify a violation against the baseline. The three states
    /// drive both the filter (`Covered` drops, `Regressed` / `New`
    /// keep) and the stderr tag prefix in `run_check`. Keeping the
    /// recorded value on the kept variant means the renderer can
    /// compute the `+N%` regression magnitude without re-keying.
    ///
    /// Matching (issue #377) resolves a *recorded value* in priority
    /// order: (1) the `(path, qualified, metric)` symbol group, with the
    /// start-line tolerance breaking ties when the symbol is ambiguous;
    /// (2) when `fuzzy` is on and the symbol did not match, an exact
    /// body-hash match within the same `(path, metric)` — this absorbs a
    /// rename that kept the body shape. A resolved value then runs the
    /// same `<=` ratchet as before.
    ///
    /// NaN current values are classified as `Regressed` rather than
    /// `Covered`: a NaN metric is degenerate (typically a Halstead
    /// edge case on trivial functions) and falling through to the
    /// `<=` arm would silently mark it covered because `NaN <= x` is
    /// false. The explicit guard makes the intent obvious at the
    /// call site, and the renderer keys off `value.is_nan()` to emit
    /// `[regr NaN]` instead of dividing by `recorded`.
    pub(crate) fn classify(&self, v: &Violation) -> Coverage {
        let path = normalize_path(&self.anchor, &v.path);
        let qualified = if self.legacy_symbol_match {
            bare_name(&v.function).to_owned()
        } else {
            v.function.clone()
        };
        let key = SymbolKey {
            path,
            qualified,
            metric: v.metric.to_string(),
        };
        let recorded = self
            .match_in_group(&key, v)
            .or_else(|| self.match_by_hash(&key, v));
        match recorded {
            None => Coverage::New,
            Some(recorded) if v.value.is_nan() => Coverage::Regressed { recorded },
            Some(recorded) if v.value <= recorded => Coverage::Covered { recorded },
            Some(recorded) => Coverage::Regressed { recorded },
        }
    }

    /// Resolve the recorded value within the `key`'s symbol group.
    /// A lone record matches unconditionally (line-drift tolerant — the
    /// whole point of #377). An ambiguous group prefers an exact
    /// body-hash match (when fuzzy), then the record whose `start_line`
    /// is closest to the violation and within `tolerance`.
    fn match_in_group(&self, key: &SymbolKey, v: &Violation) -> Option<f64> {
        let records = self.by_symbol.get(key)?;
        if let [only] = records.as_slice() {
            return Some(only.value);
        }
        if self.fuzzy
            && let Some(h) = v.body_hash
            && let Some(rec) = records.iter().find(|r| r.body_hash == Some(h))
        {
            return Some(rec.value);
        }
        records
            .iter()
            .filter_map(|r| {
                let dist = r.start_line.abs_diff(v.start_line);
                (dist <= self.tolerance).then_some((dist, r.value))
            })
            // Closest recorded line wins. On an exact distance tie — two
            // equidistant records for a genuinely ambiguous symbol —
            // prefer the higher recorded value so an unplaceable
            // violation errs toward `Covered` rather than a spurious
            // regression. Deterministic regardless of entry order.
            .min_by(|a, b| a.0.cmp(&b.0).then_with(|| b.1.total_cmp(&a.1)))
            .map(|(_, value)| value)
    }

    /// Rename-tolerant fallback: when the qualified symbol did not match
    /// (the function was renamed or moved containers) but the body shape
    /// is unchanged, match on the normalised body hash within the same
    /// `(path, metric)`. Gated behind `fuzzy`; a no-op otherwise.
    fn match_by_hash(&self, key: &SymbolKey, v: &Violation) -> Option<f64> {
        if !self.fuzzy {
            return None;
        }
        let h = v.body_hash?;
        self.by_hash
            .get(&(key.path.clone(), key.metric.clone(), h))
            .copied()
    }
}

/// Outcome of comparing a single violation against the loaded baseline.
///
/// `Covered` drops the violation from the kept set (the function has
/// not worsened past its recorded value, and we're explicitly tolerating
/// it as ratchet-down tech debt). `Regressed` and `New` keep the
/// violation and carry enough information for the stderr renderer to
/// prefix a `[regr +N%]` / `[new]` tag at emit time.
#[derive(Debug, Clone, Copy, PartialEq)]
pub(crate) enum Coverage {
    /// Baseline entry exists and current value has not worsened.
    /// `recorded` is the value pinned in the baseline file.
    Covered { recorded: f64 },
    /// Baseline entry exists but the current value has worsened.
    /// `recorded` is the prior value; the renderer subtracts to
    /// compute the regression magnitude.
    Regressed { recorded: f64 },
    /// No baseline record matched this violation by qualified symbol
    /// (within tolerance) or, when fuzzy matching is on, by body hash —
    /// the violation is new since the baseline was written.
    New,
}

pub(crate) fn from_violations(
    violations: Vec<Violation>,
    anchor: &Path,
    provenance: Provenance,
) -> BaselineFile {
    // Defensive anchor normalisation — mirrors `Baseline::from_str` so
    // write and read sides agree even when a caller bypasses
    // `anchor_for` and supplies an un-normalised path.
    let anchor = lexical_normalize(anchor);
    let mut entries: Vec<BaselineEntry> = violations
        .into_iter()
        .filter_map(|v| {
            if !v.value.is_finite() {
                eprintln!(
                    "warning: skipping non-finite value for {}:{}-{}: {} = {}",
                    v.path.display(),
                    v.start_line,
                    v.end_line,
                    v.metric,
                    v.value,
                );
                return None;
            }
            // Drop negative values (including `-0.0`, which `< 0.0`
            // would miss under IEEE 754) so write and read sides stay
            // round-trip-symmetric — `from_str` filters the same set.
            // A genuinely-negative `recorded` (e.g. `-1.0` from a
            // hand-edited TOML) would also produce a double-signed
            // `[regr +-N%]` tag downstream; `-0.0` is benign for the
            // renderer but dropped here to keep both filters identical.
            // Production metric extractors never emit negative values,
            // so this only matters for hand-crafted Violations and
            // adversarial TOML inputs.
            if v.value.is_sign_negative() {
                eprintln!(
                    "warning: skipping negative value for {}:{}-{}: {} = {}",
                    v.path.display(),
                    v.start_line,
                    v.end_line,
                    v.metric,
                    v.value,
                );
                return None;
            }
            Some(BaselineEntry {
                path: normalize_path(&anchor, &v.path),
                qualified: v.function,
                start_line: v.start_line,
                metric: v.metric.to_string(),
                value: v.value,
                body_hash: v.body_hash.map(encode_body_hash),
            })
        })
        .collect();
    // Sort by `qualified` ahead of `start_line` so the records that
    // share a symbol (the disambiguation case) cluster together in the
    // rendered file, which is what a reviewer reads.
    entries.sort_by(|a, b| {
        a.path
            .cmp(&b.path)
            .then(a.qualified.cmp(&b.qualified))
            .then(a.start_line.cmp(&b.start_line))
            .then(a.metric.cmp(&b.metric))
    });
    BaselineFile {
        version: Some(BASELINE_VERSION),
        provenance: Some(provenance),
        entries,
    }
}

/// Render a `BaselineFile` to a TOML string with the standard comment
/// header prepended. Output is deterministic byte-for-byte for the
/// same input (TOML's f64 formatter is round-trip stable, struct field
/// order is fixed by `#[derive(Serialize)]` declaration order).
pub(crate) fn render(file: &BaselineFile) -> Result<String, toml::ser::Error> {
    let body = toml::to_string(file)?;
    Ok(format!("{HEADER}{body}"))
}

/// The bare (innermost) name of a qualified symbol: the segment after
/// the last `::`, or the whole string when there is no separator. Used
/// to match a v4 violation's qualified symbol (`MyStruct::do_thing`)
/// against a legacy v2/v3 baseline entry that stored only `do_thing`,
/// and to elide a function's own name from its body hash.
pub(crate) fn bare_name(qualified: &str) -> &str {
    qualified
        .rsplit_once("::")
        .map_or(qualified, |(_, tail)| tail)
}

/// Encode a body hash as the lowercase, zero-padded 16-digit hex form
/// stored in the TOML. Hex (not a TOML integer) because FNV-1a fills
/// the full `u64` range and TOML integers are `i64`.
fn encode_body_hash(h: u64) -> String {
    format!("{h:016x}")
}

/// Decode a stored body hash. Returns `None` for any malformed digest
/// (wrong length, non-hex) so a hand-edited file degrades the entry to
/// "no fuzzy fallback" rather than aborting the whole load.
fn decode_body_hash(s: &str) -> Option<u64> {
    (s.len() == 16)
        .then(|| u64::from_str_radix(s, 16).ok())
        .flatten()
}

/// FNV-1a offset basis and prime for the 64-bit variant. FNV is chosen
/// over the std `DefaultHasher` because the digest is persisted to disk
/// and must be byte-stable across bca versions, platforms, and process
/// runs — `DefaultHasher`'s algorithm and seed carry no such guarantee.
const FNV_OFFSET_BASIS: u64 = 0xcbf2_9ce4_8422_2325;
const FNV_PRIME: u64 = 0x0000_0100_0000_01b3;

/// Hash the normalised body of the space spanning `start_line..=end_line`
/// (1-based, inclusive) within `source`, with the function's own `name`
/// elided. This is the "fuzzy" in fuzzy matching: a digest of a view of
/// the body that survives the two changes a rename-or-relocate refactor
/// makes (issue #377, rule 3) while still distinguishing genuinely
/// different code:
///
/// 1. **Whitespace**: `\r` is dropped, each line's internal whitespace
///    runs collapse to one space, leading/trailing whitespace is
///    trimmed, and blank lines are skipped — so reformatting,
///    re-indentation, or blank-line churn does not change the digest.
/// 2. **The function's own name**: every whole-word occurrence of `name`
///    (the declaration and any recursive self-calls) is replaced with a
///    fixed sentinel, so renaming `classify` to `categorize` leaves the
///    digest unchanged. Whole-word means bounded by non-identifier bytes,
///    so a `name` of `is` does not corrupt `is_valid`.
///
/// Out-of-range lines are clamped (a `start_line` past EOF yields the
/// empty-body digest), so a malformed span never panics.
pub(crate) fn hash_body(source: &[u8], start_line: usize, end_line: usize, name: &str) -> u64 {
    let normalized = normalize_body(source, start_line, end_line);
    let elided = elide_identifier(&normalized, name.as_bytes());
    let mut hash = FNV_OFFSET_BASIS;
    for &b in &elided {
        hash ^= u64::from(b);
        hash = hash.wrapping_mul(FNV_PRIME);
    }
    hash
}

/// Build the whitespace-normalised byte view of a line span (step 1 of
/// [`hash_body`]). Lines are joined by `\n`; blank lines are dropped.
fn normalize_body(source: &[u8], start_line: usize, end_line: usize) -> Vec<u8> {
    let first = start_line.saturating_sub(1);
    let mut out = Vec::new();
    for line in source
        .split(|&b| b == b'\n')
        .skip(first)
        .take(end_line.saturating_sub(first))
    {
        let mut line_started = false;
        let mut pending_space = false;
        for &b in line {
            if b == b'\r' {
                continue;
            }
            if b.is_ascii_whitespace() {
                pending_space = true;
                continue;
            }
            if line_started && pending_space {
                out.push(b' ');
            }
            pending_space = false;
            out.push(b);
            line_started = true;
        }
        if line_started {
            out.push(b'\n');
        }
    }
    out
}

/// Sentinel emitted in place of the elided function name. `\x00` does not
/// appear in normalised source bytes, so it cannot collide with real
/// content.
const ELIDED_NAME_SENTINEL: u8 = 0x00;

/// Replace every whole-word occurrence of `name` in `haystack` with
/// [`ELIDED_NAME_SENTINEL`] (step 2 of [`hash_body`]). Whole-word means
/// neither neighbour is an identifier byte. An empty or non-identifier
/// `name` (e.g. the `<file>` / `<anon@L..>` sentinels) is left as-is.
fn elide_identifier(haystack: &[u8], name: &[u8]) -> Vec<u8> {
    let is_ident = |b: u8| b.is_ascii_alphanumeric() || b == b'_';
    if name.is_empty() || !name.iter().copied().all(is_ident) {
        return haystack.to_vec();
    }
    let mut out = Vec::with_capacity(haystack.len());
    let mut i = 0;
    while i < haystack.len() {
        let left_ok = i == 0 || !is_ident(haystack[i - 1]);
        let matches = left_ok
            && haystack[i..].starts_with(name)
            && haystack.get(i + name.len()).is_none_or(|&b| !is_ident(b));
        if matches {
            out.push(ELIDED_NAME_SENTINEL);
            i += name.len();
        } else {
            out.push(haystack[i]);
            i += 1;
        }
    }
    out
}

/// Derive the canonical anchor for a baseline file at `baseline_path`:
/// the lexically-absolute directory the file lives in. Used at both
/// write and read time so the key shape is independent of `--paths`
/// form, working directory drift, or whether the path was passed
/// relative or absolute.
///
/// Lexical, not symlink-following: `bca` should not surprise users by
/// resolving `src/` through a symlinked directory. The cost is that a
/// baseline written via a symlinked invocation and read via the real
/// path (or vice-versa) does not match — but that mirrors how every
/// other tool (cargo, git) treats workdir identity.
pub(crate) fn anchor_for(baseline_path: &Path) -> PathBuf {
    // `std::path::absolute` only fails when the path is empty or the
    // platform cannot obtain the CWD (effectively never in a normal
    // shell). Fall back to the input path so the rest of the pipeline
    // degrades to pre-#376 behaviour (no canonicalisation) instead of
    // dying — the worst case is the path-stickiness this fix targets,
    // not a hard error.
    let abs = std::path::absolute(baseline_path).unwrap_or_else(|_| baseline_path.to_path_buf());
    let mut abs = lexical_normalize(&abs);
    // `pop` returns false if the path is already a root or empty; in
    // that degenerate case the path itself becomes the anchor.
    abs.pop();
    abs
}

/// Lexically normalise `p` by folding `.` and `..` components without
/// touching the filesystem. POSIX-style folding:
/// - `..` after a `Normal` component pops it (`a/b/../c` → `a/c`).
/// - `..` immediately after a `RootDir` or Windows `Prefix` is a no-op
///   (`/..` → `/`, `C:\..` → `C:\`) — you cannot go above the root.
/// - `..` with no prior component to consume is preserved literally
///   (`../a` → `../a`, `a/../../b` → `../b`). This keeps identity for
///   baselines that legitimately reference a sibling of the anchor.
fn lexical_normalize(p: &Path) -> PathBuf {
    let mut out = PathBuf::new();
    for c in p.components() {
        match c {
            Component::Prefix(_) | Component::RootDir | Component::Normal(_) => out.push(c),
            Component::CurDir => {}
            Component::ParentDir => match out.components().next_back() {
                // Pop the previous Normal component (typical case).
                Some(Component::Normal(_)) => {
                    out.pop();
                }
                // POSIX/Windows: `..` past a root or drive prefix is
                // a no-op. `/..` resolves to `/`, not `/..`. Without
                // this case the normalised path would be non-canonical
                // and downstream `strip_prefix` would mis-match.
                Some(Component::RootDir | Component::Prefix(_)) => {}
                // No prior Normal/Root/Prefix component (e.g.,
                // relative path starting with `..` or accumulating
                // multiple leading `..`s). Preserve the `..` literally
                // so a baseline that legitimately points at a sibling
                // of the anchor keeps a distinct identity.
                _ => out.push(c),
            },
        }
    }
    out
}

/// Normalize a path for use as a baseline identity key.
///
/// 1. The path is made lexically absolute (using `anchor` as the base
///    directory when relative) and `.` / `..` components are folded.
/// 2. If the result is under `anchor`, the anchor prefix is stripped so
///    the key form is `src/foo.rs` rather than `{anchor}/src/foo.rs`.
///    Paths outside the anchor (rare; e.g. a baseline that records
///    files from a sibling crate) keep their absolute form.
/// 3. The resulting `OsStr` is fed through the byte-level
///    percent-encoder for TOML safety (backslash → forward slash,
///    non-unreserved bytes → `%XX`, Windows unpaired surrogates →
///    `%uHHHH`). The encoding is injective: distinct byte sequences
///    produce distinct strings.
///
/// Non-UTF-8 paths cannot be represented verbatim in a TOML string
/// (TOML mandates UTF-8). Falling back to `Path::display()` would
/// replace every invalid byte with U+FFFD and collapse distinct paths
/// onto the same key — exactly the lossy identity collision we have to
/// avoid. The per-byte encoder preserves identity by emitting `%XX` for
/// every byte not in the unreserved path set.
fn normalize_path(anchor: &Path, p: &Path) -> String {
    // Resolve to an absolute, lexically-normalised PathBuf so the key
    // is independent of CWD and the `--paths` form the user passed.
    // An already-absolute `p` and an empty anchor both bypass the join
    // (empty-anchor is the in-memory-test case; absolute is the
    // `--paths "$PWD"` form).
    let abs = if p.is_absolute() || anchor.as_os_str().is_empty() {
        lexical_normalize(p)
    } else {
        lexical_normalize(&anchor.join(p))
    };
    let stripped = abs.strip_prefix(anchor).unwrap_or(&abs);
    encode_os_path(stripped.as_os_str())
}

/// Encode an OS string into a TOML-safe key. Routes through the
/// UTF-8 fast path when possible (still per-byte percent-encoded for
/// `%` safety), falls back to the platform-specific byte/WTF-16
/// encoders for non-UTF-8 paths. See [`normalize_path`] for the
/// injectivity guarantee.
fn encode_os_path(s: &std::ffi::OsStr) -> String {
    match s.to_str() {
        Some(s) => {
            let mut out = String::with_capacity(s.len());
            for b in s.bytes() {
                let b = if b == b'\\' { b'/' } else { b };
                push_percent_encoded_byte(&mut out, b);
            }
            out
        }
        None => encode_non_utf8_os_str(s),
    }
}

#[cfg(unix)]
fn encode_non_utf8_os_str(s: &std::ffi::OsStr) -> String {
    use std::os::unix::ffi::OsStrExt;
    percent_encode_path_bytes(s.as_bytes())
}

#[cfg(windows)]
fn encode_non_utf8_os_str(s: &std::ffi::OsStr) -> String {
    use std::os::windows::ffi::OsStrExt;
    percent_encode_wtf16(s.encode_wide())
}

#[cfg(not(any(unix, windows)))]
fn encode_non_utf8_os_str(s: &std::ffi::OsStr) -> String {
    // Exotic targets (wasm, etc.) where neither `OsStrExt` is available.
    // Reuse the per-byte encoder on the lossy UTF-8 form so output is
    // still TOML-safe; injectivity is best-effort here because the
    // platform itself has already destroyed the original bytes via
    // `to_string_lossy`. Prefix with U+FFFD so the key can never collide
    // with one produced through the `to_str()` branch above.
    let mut out = String::from("\u{FFFD}");
    for &b in s.to_string_lossy().as_bytes() {
        push_percent_encoded_byte(&mut out, b);
    }
    out
}

/// Percent-encode the raw bytes of a non-UTF-8 path so the result is
/// (1) valid UTF-8 (required by TOML), (2) injective for distinct byte
/// sequences (required to keep baseline identities from collapsing),
/// and (3) human-recognizable for the common case where most bytes are
/// printable ASCII path characters. The unreserved set mirrors the
/// "safe for use in a filename" subset of RFC 3986 unreserved with
/// `/` added (path separator) and `%` excluded (escape introducer).
#[cfg(unix)]
fn percent_encode_path_bytes(bytes: &[u8]) -> String {
    let mut out = String::with_capacity(bytes.len());
    for &b in bytes {
        push_percent_encoded_byte(&mut out, b);
    }
    out
}

/// Append a single byte to `out`, either verbatim (if it falls in the
/// unreserved path set) or as `%XX` (uppercase hex). The `%` byte
/// itself is not unreserved, so the output is unambiguous: every `%`
/// in the result was emitted by this function and is followed by
/// either two hex digits (from this function) or `u` followed by four
/// hex digits (from [`percent_encode_wtf16`]).
fn push_percent_encoded_byte(out: &mut String, b: u8) {
    use std::fmt::Write;
    let is_unreserved = b.is_ascii_alphanumeric()
        || matches!(
            b,
            b'-' | b'_' | b'.' | b'~' | b'/' | b':' | b'+' | b',' | b' '
        );
    if is_unreserved {
        out.push(b as char);
    } else {
        // Writing to a String can only fail on allocation failure, which
        // already panics in the standard library.
        let _ = write!(out, "%{b:02X}");
    }
}

/// Percent-encode a WTF-16 code-unit sequence into a TOML-safe UTF-8
/// string. Valid scalar values are encoded as their UTF-8 bytes through
/// [`push_percent_encoded_byte`]; unpaired surrogates are emitted as
/// `%uHHHH` (uppercase 4-digit hex), a form the byte encoder never
/// produces. The result is:
///
/// 1. **Injective**: every code unit maps to a distinct token (either
///    a sequence of `%XX` byte escapes / unreserved bytes for a paired
///    scalar, or one `%uHHHH` for an unpaired surrogate). Two distinct
///    WTF-16 sequences therefore always produce distinct strings.
/// 2. **Stable**: deterministic; no allocation order or hashing
///    influences output.
/// 3. **Human-debuggable enough**: ASCII path components survive
///    unchanged.
///
/// Exposed at `pub(crate)` purely so the unit tests can drive it with
/// synthetic input on any platform (the production caller is
/// `#[cfg(windows)]` only).
#[cfg(any(windows, test))]
pub(crate) fn percent_encode_wtf16(units: impl IntoIterator<Item = u16>) -> String {
    use std::fmt::Write;

    let mut out = String::new();
    let mut buf = [0u8; 4];
    for r in char::decode_utf16(units) {
        match r {
            Ok(c) => {
                for &b in c.encode_utf8(&mut buf).as_bytes() {
                    push_percent_encoded_byte(&mut out, b);
                }
            }
            Err(e) => {
                let _ = write!(out, "%u{:04X}", e.unpaired_surrogate());
            }
        }
    }
    out
}

